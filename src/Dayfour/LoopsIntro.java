package Dayfour;

public class LoopsIntro {

    public static void main(String[] args) {

        int count = 1;
        while (count <=5) {
            System.out.println("Count is: " + count);
            count++;                                            //lisab count'ile koguaeg ühe ja lõpetab, kui on võrdne viiega
            System.out.println("Count is after count++: " + count);
        }


    }
}
